-- Data Control Language (DCL) example statements. DCL is used to control access to a database. It is responsible for defining the rights and roles granted to individual users.
GRANT
REVOKE

-- Data Definition Language (DDL) example statements. DDL is used to defined the schema of a relation, like creating, modifying, or deleting databases and tables. DDL is responsible for describing how data is structured.
CREATE
DROP
ALTER
ADD

-- Data Manipulation Language (DML) example statements. DML is used to create and manipulate the data of a relation, like any of the CRUD (create, read, update, delete) operations on the actual data in a database.
INSERT
DELETE
SELECT